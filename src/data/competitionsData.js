export function getStatus(item) {
  const now   = Date.now()
  const start = item.startDateISO ? new Date(item.startDateISO).getTime() : 0
  const end   = item.endDateISO   ? new Date(item.endDateISO).getTime()   : Infinity
  if (now < start) return 'soon'
  if (now > end)   return 'closed'
  return 'open'
}

